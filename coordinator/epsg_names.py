# Vertical: NAVD88 height datums (the units the State Plane zones pair with).
EPSG_VERTICAL_NAMES: dict[str, str] = {
    "5703": "NAVD88 height (m)",
    "6360": "NAVD88 height (ftUS)",
    "8228": "NAVD88 height (ft)",
}

# Merged view the API hands to the web form (horizontal ∪ vertical).
EPSG_NAMES: dict[str, str] = {**EPSG_HORIZONTAL_NAMES, **EPSG_VERTICAL_NAMES}


def epsg_name(code: str | int | None) -> str:
    """Return the display name for an EPSG code, or "" if unknown."""
    if code is None:
        return ""
    return EPSG_NAMES.get(str(code).strip(), "")
